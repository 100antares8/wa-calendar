const FEATURE_ORDER: ("proverb" | "figure" | "tea" | "nature")[] = [
  "proverb", "figure", "tea", "nature",
];

export function getDailySeasonalBundle(jst: { y: number; m: number; d: number }): {
  proverb: SeasonalLine;
  figure: SeasonalLine;
  tea: SeasonalLine;
  nature: SeasonalLine;
  featured: SeasonalLine;
  featuredKind: (typeof FEATURE_ORDER)[number];
} {
  const s = seed(jst.y, jst.m, jst.d);
  const proverb = pick(PROVERBS, s, 1);
  const figure = pick(FIGURES, s, 2);
  const tea = pick(TEA, s, 3);
  const nature = pick(NATURE, s, 4);
  const featuredKind = FEATURE_ORDER[s % 4];
  const featured = { proverb, figure, tea, nature }[featuredKind];
  return { proverb, figure, tea, nature, featured, featuredKind };
}
