function seed(y: number, m: number, d: number): number {
  return y * 10000 + m * 100 + d;
}

function pick<T>(arr: T[], s: number, salt: number): T {
  const x = Math.abs((s * 31 + salt * 17) % arr.length);
  return arr[x];
}

